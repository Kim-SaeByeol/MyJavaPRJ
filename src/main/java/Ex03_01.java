public class Ex03_01 {
    /* pg 88
    교재는 자바프로그래밍 for Beginner (한빛아카데미 우재남 지음)
     */
    public static void main(String[] args) {
        System.out.printf("100+100");       // "" 안의 문자열은 그냥 출력
        System.out.printf("\n");            // \n 은 행을 바꿈을 뜻함.
        System.out.printf("%d", 100+100);   // %d 의 값이 100+100. 즉 200 임을 뜻함.
        System.out.printf("\n");            // \n 은 행을 바꿈을 뜻함.
    }
}
